    override init () {
        super.init()
        
        var error : NSError?
        
        var regex: NSRegularExpression?
        
        
        // Swift color with predefined name and alpha value specified
        
        let commonSwiftRegex = "(NS|UI)Color\\.([a-z0-9][A-Za-z0-9]+)Color\\(\\s*"
        
        do {
            regex = try NSRegularExpression ( pattern: commonSwiftRegex + "alpha:\\s*" + swiftAlphaConst + "\\s*\\)", options: [])
        } catch let error1 as NSError {
            error = error1
            regex = nil
        }
        
        if regex == nil {
            print ( "Error creating Swift predefined color with alpha regex = \(error?.localizedDescription)" )
        } else {
            regexes.append( regex! )
        }

        
        do {
            // Swift color with predefined name and no alpha
        
            regex = try NSRegularExpression ( pattern: commonSwiftRegex + "\\)", options: [])
        } catch let error1 as NSError {
            error = error1
            regex = nil
        }
            
        if regex == nil {
            print ( "Error creating Swift predefined color without alpha regex = \(error?.localizedDescription)" )
        } else {
            regexes.append( regex! )
        }
        
        
        // Objective-C
        
        let commonObjCRegex = "\\[\\s*(NS|UI)Color\\s*([a-z0-9][A-Za-z0-9]+)Color"
        
        do {
            // Objective-C color with predefined name with alpha
        
            regex = try NSRegularExpression ( pattern: commonObjCRegex + "WithAlpha:\\s*" + objcAlphaConst + "\\s*\\]", options: [])
        } catch let error1 as NSError {
            error = error1
            regex = nil
        }
        
        if regex == nil {
            print ( "Error creating Objective-C predefined color regex = \(error?.localizedDescription)" )
        } else {
            regexes.append( regex! )
        }

        
        do {
            // Objective-C color with predefined name and no alpha
        
            regex = try NSRegularExpression ( pattern: commonObjCRegex + "\\s*\\]", options: [])
        } catch let error1 as NSError {
            error = error1
            regex = nil
        }
        
        if regex == nil {
            print ( "Error creating Objective-C predefined color with no alpha regex = \(error?.localizedDescription)" )
        } else {
            regexes.append( regex! )
        }
    }

    
    override func processMatch(match: NSTextCheckingResult, line: String) -> SearchResult? {
        
        if ( ( match.numberOfRanges == 3 ) || ( match.numberOfRanges == 4 ) ) {
            var alphaValue : CGFloat = 1.0
            let matchString = stringFromRange( match.range, line: line )
            let colorTypeString = stringFromRange( match.rangeAtIndex( 1 ), line: line )
            let colorName = stringFromRange( match.rangeAtIndex( 2 ), line: line )
            var capturedStrings = [ matchString, colorTypeString, colorName ]
            
            if ( match.numberOfRanges == 4 ) {
                let alphaString = stringFromRange( match.rangeAtIndex( 3 ), line: line )
                alphaValue = CGFloat ( (alphaString as NSString).doubleValue )
                capturedStrings.append( alphaString )
            }
            
            
            if var color = colorDictionary[ colorName ] {
                
                if ( alphaValue != 1.0 ) {
                    color = color.colorWithAlphaComponent( alphaValue )
                }
                
                var searchResult = SearchResult ( color: color, textCheckingResult: match, capturedStrings: capturedStrings )
                searchResult.creationType = .PredefinedColor
                searchResult.colorType = colorTypeFromString( colorTypeString )
                searchResult.language = programmingLanguageFromString( matchString )
                
                return searchResult
            }
        }
        
        return nil
    }
